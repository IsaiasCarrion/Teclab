import pandas as pd
import numpy as np

# Suma de arrays

# Definimos los arrays A y B
A = np.array([[1, 2, 3], [4, 5, 6]])
B = np.array([[10, 11, 12], [13, 14, 15]])

# Calculamos la suma de A y B, almacenando el resultado en C
C = A + B

# Mostramos el resultado
print("Resultado de la suma (C):")
print(C)
